package e2VariablesAndTypes;
/*Viết chương trình Java để khởi tạo 2 kiểu dữ liệu double và float.
Định nghĩa rằng kiểu dữ liệu thứ hai được khai báo là kiểu float và in các giá trị ra 2 dòng khác nhau
Đầu vào: không có
Đầu ra: số kiểu float và số kiểu double, mỗi số trên một dòng
Thời gian chạy:  1.5 giây*/
public class f06FloatingPointVariables {
    public static void main(String[] args) {
        double myDouble = 5.5;
        float myFloat = 5.5f;

        System.out.println(myDouble);
        System.out.println(myFloat);
    }
}
