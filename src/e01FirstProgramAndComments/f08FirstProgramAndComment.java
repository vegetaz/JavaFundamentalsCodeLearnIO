package e01FirstProgramAndComments;
/*
* Bạn hãy viết chương trình hiển thị ra màn hình thông tin sau:
* 2468 + 1234 = {P1}
* 2468 - 1234 = {P2}
* 2468 * 1234 = {P3}
* 2468 / 1234 = {P4}
* Trong đó:
* {P1} là tổng của 2468 và 1234.
* {P2} là hiệu của 2468 và 1234.
* {P3} là tích của 2468 và 1234.
* {P4} là thương của 2468 và 1234.
* */
public class f08FirstProgramAndComment {
    public static void main(String[] args) {
        System.out.println("2468 + 1234 = " + (2468 + 1234));
        System.out.println("2468 - 1234 = " + (2468 - 1234));
        System.out.println("2468 * 1234 = " + (2468 * 1234));
        System.out.println("2468 / 1234 = " + (2468 / 1234));
    }
}
