package e02VariablesAndTypes;
/*
* Cho trước hình chữ nhật có chiều dài và chiều rộng lần lượt là 7.5 và 3.8.
* Bạn hãy viết chương trình hiển thị diện tích của hình chữ nhật này ra màn hình giống như sau:
* Area = {P}
* Với {P} là diện tích của hình chữ nhật.*/
public class F15VariableAndType {
    public static void main(String[] args) {
        float a = 7.5f;
        float b = 3.8f;
        System.out.println("Area = " + a * b);
    }
}
